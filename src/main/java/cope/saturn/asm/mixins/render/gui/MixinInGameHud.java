package cope.saturn.asm.mixins.render.gui;

import cope.saturn.core.Saturn;
import cope.saturn.core.events.RenderHUDEvent;
import net.minecraft.client.gui.hud.InGameHud;
import net.minecraft.client.util.math.MatrixStack;
import org.spongepowered.asm.mixin.Mixin;
import org.spongepowered.asm.mixin.injection.At;
import org.spongepowered.asm.mixin.injection.Inject;
import org.spongepowered.asm.mixin.injection.callback.CallbackInfo;

@Mixin(InGameHud.class)
public class MixinInGameHud {
    @Inject(method = "render", at = @At("TAIL"), cancellable = true)
    public void render(MatrixStack matrices, float tickDelta, CallbackInfo info) {
        Saturn.EVENT_BUS.post(new RenderHUDEvent(matrices));
    }
}
